     private void initializeService() {
        try {
            mahasiswaService = new MysqlMahasiswaService();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                "Gagal menghubungkan ke database: " + e.getMessage(), 
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    private void setupListModels() {
        listModelID = new DefaultListModel<>();
        listModelNama = new DefaultListModel<>();
        jListItem1.setModel(listModelID);
        jListItem2.setModel(listModelNama);
    }
    private void refreshData() {
        try {
            // Refresh connection service terlebih dahulu
            mahasiswaService.refreshConnection();
        
            List<Mahasiswa> mahasiswaList = mahasiswaService.getAll();
            listModelID.clear();
            listModelNama.clear();
        
            for (Mahasiswa mhs : mahasiswaList) {
                listModelID.addElement(String.valueOf(mhs.getId()));
                listModelNama.addElement(mhs.getNama());
            }
        
            System.out.println("Data berhasil di-refresh. Total: " + mahasiswaList.size() + " records");
        
        } catch (Exception e) {
            System.err.println("Error detail: " + e.getMessage());
            e.printStackTrace();
        
            JOptionPane.showMessageDialog(this, 
                "Gagal mengambil data: " + e.getMessage() + 
                "\n\nSilakan cek:\n1. Koneksi database\n2. Tabel mahasiswa exists\n3. MySQL service berjalan", 
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

