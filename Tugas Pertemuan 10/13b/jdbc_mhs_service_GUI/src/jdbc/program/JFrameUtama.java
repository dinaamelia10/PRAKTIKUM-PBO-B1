    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        try {
            String idText = jTextField2.getText().trim();
            String namaText = jTextField3.getText().trim();
            
            if (idText.isEmpty() || namaText.isEmpty()) {
                JOptionPane.showMessageDialog(this, 
                    "ID dan Nama harus diisi!", 
                    "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            int id = Integer.parseInt(idText);
            Mahasiswa mahasiswa = new Mahasiswa(id, namaText);
            
            mahasiswaService.update(mahasiswa);
            JOptionPane.showMessageDialog(this,"Data berhasil diupdate!", "Info", JOptionPane.INFORMATION_MESSAGE);
            refreshData();
            jTextField2.setText("");
            jTextField3.setText("");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, 
                "ID harus berupa angka!", 
                "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                "Error: " + e.getMessage(), 
                "Error", JOptionPane.ERROR_MESSAGE);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButtonEditActionPerformed

    private void jButtonTambahkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTambahkanActionPerformed
        try {
            String nama = jTextField1.getText().trim();
            
            if (nama.isEmpty()) {
                JOptionPane.showMessageDialog(this, 
                    "Nama harus diisi!", 
                    "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            Mahasiswa mahasiswa = new Mahasiswa(0, nama); // ID akan di-generate otomatis
            mahasiswaService.insert(mahasiswa);
            
            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!", "Info", JOptionPane.INFORMATION_MESSAGE);
            refreshData();
            jTextField1.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                "Error: " + e.getMessage(), 
                "Error", JOptionPane.ERROR_MESSAGE);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButtonTambahkanActionPerformed

    private void jButtonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHapusActionPerformed
        try {
            String idText = jTextField4.getText().trim();
            
            if (idText.isEmpty()) {
                JOptionPane.showMessageDialog(this, 
                    "ID harus diisi!", 
                    "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            int id = Integer.parseInt(idText);
            
            int confirm = JOptionPane.showConfirmDialog(this, 
                "Apakah Anda yakin ingin menghapus data dengan ID " + id + "?", 
                "Konfirmasi", JOptionPane.YES_NO_OPTION);
            
             if (confirm == JOptionPane.YES_OPTION) {
                mahasiswaService.delete(id);
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!", "Info", JOptionPane.INFORMATION_MESSAGE);
                refreshData();
                jTextField4.setText("");
                jTextField5.setText("");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ID harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jButtonHapusActionPerformed

    private void jButtonResetIndeksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonResetIndeksActionPerformed
        try {
        // Konfirmasi dulu
            int confirm = JOptionPane.showConfirmDialog(this, 
                "Apakah Anda yakin ingin mereset auto increment?\n" +
                "Ini akan mengatur ulang nomor ID otomatis.", 
                "Konfirmasi Reset", 
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);
            
            if (confirm == JOptionPane.YES_OPTION) {
                boolean success = mahasiswaService.resetAutoIncrement();
                if (success) {
                    JOptionPane.showMessageDialog(this, 
                        "Auto increment berhasil direset!", 
                        "Info", JOptionPane.INFORMATION_MESSAGE);
                
                // Refresh data setelah reset
                    refreshData();
                }
            }
        } catch (Exception e) {
            System.err.println("Error reset indeks: " + e.getMessage());
            e.printStackTrace();
        
            JOptionPane.showMessageDialog(this, 
                "Error saat reset indeks: " + e.getMessage() + 
                "\n\nPastikan:\n1. Database terhubung\n2. Tidak ada foreign key constraint", 
                "Error", JOptionPane.ERROR_MESSAGE);
        }// TODO add your handling code here:
    }//GEN-LAST:event_jButtonResetIndeksActionPerformed

